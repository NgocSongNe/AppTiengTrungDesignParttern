package com.example.apptiengtrung;

public class ChuDe {
    public int id;
    public String TieuDeTrung;
    public String TieuDeViet;
    public int HinhAnh;

    public ChuDe() {

    }

    public ChuDe (int id, String TieuDeTrung, String TieuDeViet, int HinhAnh) {
        this.id  = id;
        this.TieuDeTrung = TieuDeTrung;
        this.TieuDeViet = TieuDeViet;
        this.HinhAnh = HinhAnh;
    }
}
